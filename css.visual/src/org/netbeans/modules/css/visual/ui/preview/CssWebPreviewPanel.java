    class HTMLFileFilter extends FileFilter {

        public boolean accept(File pathname) {
            return pathname.isDirectory() ||
                   pathname.toString().endsWith("html") ||
                   pathname.toString().endsWith("xhtml");
        }

        public String getDescription() {
            return "(X)HTML Files";
        }

    }

