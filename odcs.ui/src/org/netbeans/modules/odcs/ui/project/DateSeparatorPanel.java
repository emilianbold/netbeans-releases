    @Override
    public void toggleExpandablePanel() {
        pnlContent.setVisible(!activitiesExpanded);
        activitiesExpanded = !activitiesExpanded;
        lblExpand.setIcon(activitiesExpanded ? Utils.COLLAPSE_ICON : Utils.EXPAND_ICON);
    }

    @Override
    public void mouseEnteredExpandable() {
        this.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
        lblDate.setForeground(ColorManager.getDefault().getLinkColor());
        lblExpand.setEnabled(true);
    }

    @Override
    public void mouseExitedExpandable() {
        this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        lblDate.setForeground(lblExpand.getForeground());
        lblExpand.setEnabled(false);
    }

    @Override
    public void revalidateExpandable() {
        this.revalidate();
    }

    @Override
    public synchronized void addMouseListener(MouseListener l) {
        pnlTitle.addMouseListener(l);
    }

    private String getDateText() {
        DateFormat dateFormat = DateFormat.getDateInstance(DateFormat.DEFAULT);
        return dateFormat.format(date);
    }
