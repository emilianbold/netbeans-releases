    public ReferencePanel(CommonRef ref) {
        this.ref=ref;
        initComponents();
        typeComboBox.setModel(new DefaultComboBoxModel(BINDING_REFERENCE_TYPES));
        if(ref!=null) {
            idField.setText(ref.getXmiId());
            jndiNameField.setText(ref.getJndiName());
            hrefField.setText(ref.getHref());            
            if(ref instanceof ResRefBindingsType) {
                typeComboBox.setSelectedIndex(0);
            } else if(ref instanceof EjbRefBindingsType) {
                typeComboBox.setSelectedIndex(1);
            } else if(ref instanceof ResEnvRefBindingsType) {
                typeComboBox.setSelectedIndex(2);
            }
            typeComboBox.setEnabled(false);
        }
        
    }
    
