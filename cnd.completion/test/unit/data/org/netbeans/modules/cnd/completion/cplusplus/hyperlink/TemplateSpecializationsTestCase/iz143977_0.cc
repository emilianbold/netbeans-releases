    template<class K, class V = K> struct Pair {
        K first;
        V second;
    };

    int hash(Pair<Object> pair) {
        int hash = 7;
        hash = 79 * hash + pair.first.hashCode(); // hashCode should be resolved 
        hash = 79 * hash + pair.second.hashCode(); // hashCode should be resolved 
        return hash;
    }
    
