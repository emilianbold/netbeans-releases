    private void initUrls(List<String> repositoryUrls) {
        GridBagConstraints gbc = new GridBagConstraints();
        gbc.insets = new Insets(2, 0, 3, 15);
        gbc.anchor = GridBagConstraints.NORTHWEST;
        gbc.fill = GridBagConstraints.HORIZONTAL;
        gbc.weightx = 0.1;
        gbc.gridwidth = GridBagConstraints.REMAINDER;
        for (String url : repositoryUrls) {
            JTextField field = createUrlTextField(url);
            add(field, gbc);
        }
    }

    private JTextField createUrlTextField(String url) {
        JTextField textField = new JTextField(url);
        textField.setEditable(false);
        textField.setOpaque(false);
        return textField;
    }
