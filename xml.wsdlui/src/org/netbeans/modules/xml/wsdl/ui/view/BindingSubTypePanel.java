    private void initGUI() {
        boolean isDefaultAvailable = false;
        ArrayList<String> actualTemplateNames = new ArrayList<String>();
        LocalizedTemplate[] templates = this.mLtg.getTemplate();
        if (templates.length < TEMPLATE_MAX) {
            for (int i = 0; i < templates.length; i++) {
                LocalizedTemplate template = templates[i];
            if (this.mMode == null ||(LocalizedTemplate.Mode.BOTH.equals(template.getMode())) ||
                    this.mMode.equals(template.getMode()) || template.isSkeleton() ){            
                TemplatePanel panel = new TemplatePanel(template, buttonGroup1);
                panel.getRadioButton().addActionListener(this.mButtonActionListener);
                mPanels.add(panel);
                this.add(panel);
            actualTemplateNames.add(templates[i].getName());
                if (template.getDelegate().isDefault()) {
                    setSelectedTemplateName(templates[i].getName());
                    isDefaultAvailable = true;
                }
            }
            }
        if (!isDefaultAvailable && actualTemplateNames.size() > 0 ) {
            setSelectedTemplateName(actualTemplateNames.get(0));
            }
        } else {
            tooManyTemplates = true;
            Vector<DisplayObject> templateVect = new Vector<DisplayObject>();
            LocalizedTemplate defaultTemplate = null;
            LocalizedTemplate firstTemplate = null;
            for (int i = 0; i < templates.length; i++) {
                LocalizedTemplate template = templates[i];
            if (this.mMode == null ||(LocalizedTemplate.Mode.BOTH.equals(template.getMode())) ||
                    this.mMode.equals(template.getMode()) || template.isSkeleton() ){            

                    //TemplatePanel panel = new TemplatePanel(template, buttonGroup1);
                    //panel.getRadioButton().addActionListener(this.mButtonActionListener);
                    //mPanels.add(panel);
                    //this.add(panel);
                    templateVect.add(new DisplayObject(template.getName(), template));
                    if (template.getDelegate().isDefault()) {
                        defaultTemplate = template;
                        isDefaultAvailable = true;
                    }
                    if ( firstTemplate == null) {
                        firstTemplate = template;
                    }                
                }


            }
            
            DefaultComboBoxModel defaultComboBoxModel = new DefaultComboBoxModel(templateVect);
            templateComboBox = new JComboBox(defaultComboBoxModel);
            templateComboBox.setRenderer(new MyComboBoxRenderer());
            templateComboBox.setName("templateComboBox");
            templateComboBox.getAccessibleContext().setAccessibleName("Available Configuration Templates");
            templateComboBox.getAccessibleContext().setAccessibleDescription(NbBundle.getMessage(BindingSubTypePanel.class, "BindingSubTypePanel.templateComboBox.accessibleDescription"));
            templateComboBox.setToolTipText(NbBundle.getMessage(BindingSubTypePanel.class, "BindingSubTypePanel.templateComboBox.accessibleDescription"));
            if (isDefaultAvailable && firstTemplate != null) {
                if (defaultTemplate == null) {
                    defaultTemplate = firstTemplate;
                }
                templateComboBox.setSelectedItem(new DisplayObject(defaultTemplate.getName(), defaultTemplate));
            }
        
            java.awt.GridBagConstraints gridBagConstraints = new java.awt.GridBagConstraints();
            gridBagConstraints.gridx = 0;
            gridBagConstraints.gridy = 0;
            gridBagConstraints.gridwidth = java.awt.GridBagConstraints.REMAINDER;
            gridBagConstraints.fill = java.awt.GridBagConstraints.HORIZONTAL;
            gridBagConstraints.anchor = java.awt.GridBagConstraints.WEST;
            gridBagConstraints.weightx = 0.5;        
            this.add(templateComboBox, gridBagConstraints);     

            //add listener
            templateComboBox.addActionListener(mButtonActionListener);
            
        }
       
    }
    
    class MyComboBoxRenderer extends BasicComboBoxRenderer {        
        @Override
        public Component getListCellRendererComponent(JList list,
                Object value, int index, boolean isSelected, boolean cellHasFocus) {
            LocalizedTemplate template = (LocalizedTemplate) ((DisplayObject)value).getValue();
            if (isSelected) {
                setBackground(list.getSelectionBackground());
                setForeground(list.getSelectionForeground());
                if (-1 < index) {
                    list.setToolTipText(template.getDescription());
                }
            } else {
                setBackground(list.getBackground());
                setForeground(list.getForeground());
            }
            setFont(list.getFont());
            setText((value == null) ? "" : value.toString());            
            return this;
        }
    }
        
    public LocalizedTemplate getBindingSubType() {
        if (!tooManyTemplates) {
            Iterator<TemplatePanel> it = this.mPanels.iterator();
            while (it.hasNext()) {
                TemplatePanel p = it.next();
                if (p.isSelected()) {
                    return p.getTemplate();
                }
            }
        } else {
            if (templateComboBox != null) {
                DisplayObject template = (DisplayObject) templateComboBox.getSelectedItem();
                return (LocalizedTemplate) template.getValue();
            }
        }
        return null;
    }
    
    public void reset(LocalizedTemplateGroup ltg) {
        this.removeAll();
        Iterator<TemplatePanel> it =  mPanels.iterator();
        while(it.hasNext()) {
        	TemplatePanel panel = it.next();
        	panel.getRadioButton().removeActionListener(this.mButtonActionListener);
        	
        }
        mPanels.clear();
        
        if (templateComboBox != null) {
            templateComboBox.removeActionListener(mButtonActionListener);
            templateComboBox = null;
            tooManyTemplates = false;
        }
        
        this.mLtg = ltg;
        initGUI();
        revalidate();
    }
    
    private void setSelectedTemplateName(String templateName) {
        Enumeration<AbstractButton> buttons = buttonGroup1.getElements();
        if (buttons != null) {
            while (buttons.hasMoreElements()) {
                AbstractButton button = buttons.nextElement();
                if (button.getActionCommand().equals(templateName)) {
                    button.setSelected(true);
                }
            }
        }
    }
    
