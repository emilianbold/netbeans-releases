    private void disablePanel (javax.swing.JPanel panel) 
    {
        Component[] components = panel.getComponents();
        for (Component comp : components)
        {
            if (!(comp instanceof javax.swing.JLabel)) 
            {
                if (comp.isEnabled())
                    comp.setEnabled(false);
            }
        }
    }
        
